x = float(input("enter your grade: "))

  
if x >= 90: 
        let_grade = "A" 

elif x >= 80: 
        let_grade = "B" 

elif x >= 70: 
        let_grade = "C" 

elif x >= 60: 
        let_grade = "D" 

else: 
        let_grade = "F" 

print("your grade converted to a letter is:",let_grade,) 
